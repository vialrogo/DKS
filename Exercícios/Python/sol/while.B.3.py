# B.3. Cree un programa que imprima un triángulo centrado de la altura ingresada por el usuario. Es importante notar que para que el triángulo salga bien, cada piso debe aumentar en dos el número de asteriscos (el primero tiene un asterisco, el segundo 3, el tercero 5 y así). Así el primer piso es de largo 1 y el último de largo 2n-1.  Por ejemplo:
#
# Para altura 6:
# ```
#      *
#     ***
#    *****
#   *******
#  *********
# ***********
# ```
